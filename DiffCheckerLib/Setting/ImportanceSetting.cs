        public void ImportCsv(IReadOnlyList<string> csv)
        {
            foreach (var line in csv)
            {
                var split = line.Split(',');
                try
                {
                    foreach (var importance in importances)
                    {
                        if (importance.StbName == split[0])
                        {
                            if( Importance.Required.ToJapanese() == split[1].Trim() )
                            {
                                importance.SetImportance( Importance.Required );
                                break;
                            }
                            else if (Importance.Optional.ToJapanese() == split[1].Trim())
                            {
                                importance.SetImportance(Importance.Optional);
                                break;
                            }
                            if (Importance.Unnecessary.ToJapanese() == split[1].Trim())
                            {
                                importance.SetImportance(Importance.Unnecessary);
                                break;
                            }
                            if (Importance.NotApplicable.ToJapanese() == split[1].Trim())
                            {
                                importance.SetImportance(Importance.NotApplicable);
                                break;
                            }
                        }
                    }
                }
                catch
                {
                    throw new InvalidOperationException();
                }
            }
        }

