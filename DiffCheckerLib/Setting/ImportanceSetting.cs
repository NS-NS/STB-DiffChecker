        public DataTable CreateTable()
        {
            DataTable importanceTable = new DataTable();
            var name = importanceTable.Columns.Add("StbName", typeof(string));
            name.ReadOnly = true;
            var importance = importanceTable.Columns.Add("Importance", typeof(string));
            importance.ReadOnly = true;
            foreach (var i in this.importances)
            {
                importanceTable.Rows.Add(i.StbName, i.Importance.ToJapanese());
            }

            return importanceTable;
        }
